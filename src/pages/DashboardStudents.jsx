// ── StudentForm (add / edit) ──────────────────────────────────────────────────
function StudentForm({ initial, classes, combinations, onClose }) {
  const qc    = useQueryClient()
  const toast = useToast()

  const [form, setForm] = useState({
    full_name:       initial?.full_name      ?? '',
    gender:          initial?.gender          ?? '',
    date_of_birth:   initial?.date_of_birth  ?? '',
    class_id:        initial?.class_id        ?? '',
    combination_id:  initial?.combination_id  ?? '',
    enrollment_year: initial?.enrollment_year ?? CURRENT_YEAR,
  })
  const [err, setErr] = useState('')
  const set = (k, v) => setForm(f => ({ ...f, [k]: v }))

  const selectedClass = classes.find(c => c.id === parseInt(form.class_id))
  const isALevel = selectedClass?.level === 'a_level'

  const save = useMutation({
    mutationFn: async () => {
      setErr('')
      if (!form.full_name.trim()) throw new Error('Full name is required.')
      if (!form.class_id)         throw new Error('Class is required.')

      const payload = {
        full_name:       form.full_name.trim(),
        gender:          form.gender || null,
        date_of_birth:   form.date_of_birth || null,
        class_id:        parseInt(form.class_id),
        combination_id:  isALevel && form.combination_id ? parseInt(form.combination_id) : null,
        enrollment_year: form.enrollment_year || CURRENT_YEAR,
      }

      if (initial) {
        const { error } = await supabase.from('students').update(payload).eq('id', initial.id)
        if (error) throw error
      } else {
        const { error } = await supabase.from('students').insert(payload)
        if (error) throw error
      }
    },
    onSuccess: () => {
      qc.invalidateQueries({ queryKey: ['students'] })
      toast.success(initial ? `${form.full_name} updated successfully.` : `${form.full_name} registered successfully.`)
      onClose()
    },
    onError: e => { setErr(e.message); toast.error(e.message) },
  })

  return (
    <>
      <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
        {err && (
          <div className="sm:col-span-2 flex items-start gap-2 bg-red-50 border border-red-200 text-red-700 rounded-lg px-3 py-2 text-sm">
            <AlertTriangle size={15} className="shrink-0 mt-0.5" />{err}
          </div>
        )}

        {initial?.registration_number && (
          <div className="sm:col-span-2 flex items-center gap-2 bg-gray-50 border border-gray-200 rounded-lg px-3 py-2 text-sm">
            <span className="text-gray-500 text-xs font-medium">Reg. No:</span>
            <span className="font-mono font-semibold text-gray-800">{initial.registration_number}</span>
            <span className="ml-auto text-xs text-gray-400">Auto-generated</span>
          </div>
        )}

        <Field label="Full name" required>
          <input className={inputCls} value={form.full_name}
            onChange={e => set('full_name', e.target.value)} placeholder="John Doe" />
        </Field>

        <Field label="Gender">
          <select className={inputCls} value={form.gender} onChange={e => set('gender', e.target.value)}>
            <option value="">— Select —</option>
            <option value="M">Male</option>
            <option value="F">Female</option>
          </select>
        </Field>

        <Field label="Date of birth" hint="YYYY-MM-DD">
          <input type="date" className={inputCls} value={form.date_of_birth}
            onChange={e => set('date_of_birth', e.target.value)} />
        </Field>

        <Field label="Class" required>
          <select className={inputCls} value={form.class_id}
            onChange={e => { set('class_id', e.target.value); set('combination_id', '') }}>
            <option value="">— Select class —</option>
            {['o_level','a_level'].map(lvl => (
              <optgroup key={lvl} label={lvl === 'o_level' ? 'O-Level (Form 1–4)' : 'A-Level (Form 5–6)'}>
                {classes.filter(c => c.level === lvl).map(c => (
                  <option key={c.id} value={c.id}>{c.name}</option>
                ))}
              </optgroup>
            ))}
          </select>
        </Field>

        <Field label="Enrollment year">
          <input className={inputCls} value={form.enrollment_year}
            onChange={e => set('enrollment_year', e.target.value)} placeholder={CURRENT_YEAR} maxLength={4} />
        </Field>

        {isALevel && (
          <Field label="Combination" hint="A-Level only" className="sm:col-span-2">
            <select className={inputCls} value={form.combination_id} onChange={e => set('combination_id', e.target.value)}>
              <option value="">— Select combination —</option>
              {combinations.map(c => (
                <option key={c.id} value={c.id}>{c.code} — {c.full_name}</option>
              ))}
            </select>
          </Field>
        )}
      </div>

      <div className="flex gap-3 mt-6">
        <button onClick={onClose} className="flex-1 border border-gray-300 text-gray-700 rounded-xl py-2.5 text-sm font-medium hover:bg-gray-50 transition">Cancel</button>
        <button onClick={() => save.mutate()} disabled={save.isPending}
          className="flex-1 bg-green-700 hover:bg-green-600 text-white rounded-xl py-2.5 text-sm font-semibold transition disabled:opacity-60">
          {save.isPending ? 'Saving…' : initial ? 'Save changes' : 'Register student'}
        </button>
      </div>
    </>
  )
}

// ── CSV Import Modal ──────────────────────────────────────────────────────────
function CSVImportModal({ classes, combinations, onClose }) {
  const qc      = useQueryClient()
  const toast   = useToast()
  const fileRef = useRef()

  const [rows, setRows]       = useState([])   // parsed rows
  const [errors, setErrors]   = useState([])   // per-row validation errors
  const [step, setStep]       = useState('upload') // 'upload' | 'preview'
  const [importing, setImporting] = useState(false)
  const [result, setResult]   = useState(null)  // { imported, failed }

  const classMap = useMemo(() =>
    Object.fromEntries(classes.map(c => [c.name.toLowerCase(), c])), [classes])
  const comboMap = useMemo(() =>
    Object.fromEntries(combinations.map(c => [c.code.toLowerCase(), c])), [combinations])

  const handleFile = e => {
    const file = e.target.files[0]
    if (!file) return
    if (!file.name.endsWith('.csv')) { toast.error('Please upload a .csv file.'); return }

    const reader = new FileReader()
    reader.onload = ev => {
      const { rows: parsed } = parseCSV(ev.target.result)
      const validated = parsed.map(r => {
        const errs = []
        if (!r.full_name)  errs.push('Full name required')
        if (!r.class_name) errs.push('Class name required')
        if (r.gender && !['m','f'].includes(r.gender.toLowerCase())) errs.push('Gender must be M or F')

        const cls = classMap[r.class_name?.toLowerCase()]
        if (r.class_name && !cls) errs.push(`Class "${r.class_name}" not found`)

        const combo = r.combination_code ? comboMap[r.combination_code?.toLowerCase()] : null
        if (r.combination_code && !combo) errs.push(`Combination "${r.combination_code}" not found`)

        return { ...r, _class: cls, _combo: combo, _errors: errs }
      })

      const allErrors = validated.filter(r => r._errors.length > 0)
      setRows(validated)
      setErrors(allErrors)
      setStep('preview')
    }
    reader.readAsText(file)
  }

  const handleImport = async () => {
    const valid = rows.filter(r => r._errors.length === 0)
    if (valid.length === 0) { toast.error('No valid rows to import.'); return }

    setImporting(true)
    let imported = 0, failed = 0

    for (const r of valid) {
      try {
        const payload = {
          full_name:       r.full_name.trim(),
          gender:          r.gender ? r.gender.toUpperCase() : null,
          date_of_birth:   r.date_of_birth || null,
          class_id:        r._class.id,
          combination_id:  r._combo?.id ?? null,
          enrollment_year: r.enrollment_year || CURRENT_YEAR,
        }
        const { error } = await supabase.from('students').insert(payload)
        if (error) throw error
        imported++
      } catch {
        failed++
      }
    }

    qc.invalidateQueries({ queryKey: ['students'] })
    setResult({ imported, failed, total: valid.length })
    setImporting(false)

    if (failed === 0) toast.success(`Import complete: ${imported} student(s) registered.`)
    else toast.warning(`Import done with issues: ${imported} registered, ${failed} failed.`)
  }

  if (result) {
    return (
      <>
        <div className="space-y-4">
          <div className="grid grid-cols-2 gap-3">
            {[
              { label: 'Registered', value: result.imported, color: 'bg-green-50 border-green-200 text-green-700' },
              { label: 'Failed',     value: result.failed,   color: 'bg-red-50 border-red-200 text-red-700' },
            ].map(s => (
              <div key={s.label} className={`border rounded-xl p-4 text-center ${s.color}`}>
                <p className="text-2xl font-bold">{s.value}</p>
                <p className="text-xs font-medium mt-0.5">{s.label}</p>
              </div>
            ))}
          </div>
          {result.failed > 0 && (
            <p className="text-sm text-amber-700 bg-amber-50 border border-amber-200 rounded-lg px-3 py-2">
              {result.failed} row(s) failed. Check for duplicate registration numbers or missing data.
            </p>
          )}
        </div>
        <button onClick={onClose} className="w-full mt-6 bg-green-700 hover:bg-green-600 text-white rounded-xl py-2.5 text-sm font-semibold transition">
          Done
        </button>
      </>
    )
  }

  return (
    <>
      {step === 'upload' && (
        <div className="space-y-5">
          {/* Instructions */}
          <div className="bg-blue-50 border border-blue-200 rounded-xl p-4 space-y-2">
            <p className="text-sm font-semibold text-blue-800 flex items-center gap-2">
              <FileText size={15} /> CSV Format Instructions
            </p>
            <ul className="text-xs text-blue-700 space-y-1 list-disc list-inside">
              <li>First row must be the header row (column names)</li>
              <li><strong>full_name</strong> — student's full name (required)</li>
              <li><strong>gender</strong> — M or F</li>
              <li><strong>date_of_birth</strong> — format: YYYY-MM-DD</li>
              <li><strong>class_name</strong> — must match exactly: Form 1, Form 2, … Form 6 (required)</li>
              <li><strong>combination_code</strong> — A-Level only: PCB, HEG, etc. (leave blank for O-Level)</li>
              <li><strong>enrollment_year</strong> — 4-digit year, e.g. {CURRENT_YEAR}</li>
              <li>Registration numbers are <strong>auto-generated</strong> by the system</li>
            </ul>
          </div>

          {/* Download template */}
          <button onClick={downloadTemplate}
            className="w-full flex items-center justify-center gap-2 border-2 border-dashed border-green-300
                       hover:border-green-500 text-green-700 hover:bg-green-50 rounded-xl py-3 text-sm font-semibold transition">
            <Download size={16} /> Download CSV Template
          </button>

          {/* File upload */}
          <div>
            <p className="text-sm font-medium text-gray-700 mb-2">Upload your completed CSV</p>
            <label className="flex flex-col items-center justify-center gap-2 border-2 border-dashed
                             border-gray-300 hover:border-green-400 rounded-xl py-8 cursor-pointer
                             hover:bg-gray-50 transition">
              <Upload size={24} className="text-gray-400" />
              <span className="text-sm text-gray-500">Click to select a .csv file</span>
              <input ref={fileRef} type="file" accept=".csv" className="sr-only" onChange={handleFile} />
            </label>
          </div>
        </div>
      )}

      {step === 'preview' && (
        <div className="space-y-4">
          {/* Summary */}
          <div className="grid grid-cols-3 gap-3">
            {[
              { label: 'Total rows',   value: rows.length,                          color: 'bg-gray-50 border-gray-200 text-gray-700' },
              { label: 'Valid',        value: rows.filter(r => !r._errors.length).length, color: 'bg-green-50 border-green-200 text-green-700' },
              { label: 'With errors',  value: errors.length,                        color: errors.length ? 'bg-red-50 border-red-200 text-red-700' : 'bg-gray-50 border-gray-200 text-gray-400' },
            ].map(s => (
              <div key={s.label} className={`border rounded-xl p-3 text-center ${s.color}`}>
                <p className="text-xl font-bold">{s.value}</p>
                <p className="text-xs font-medium mt-0.5">{s.label}</p>
              </div>
            ))}
          </div>

          {/* Errors */}
          {errors.length > 0 && (
            <div className="bg-red-50 border border-red-200 rounded-xl p-3 max-h-36 overflow-y-auto space-y-1">
              <p className="text-xs font-semibold text-red-700 mb-2">Rows with errors (will be skipped):</p>
              {errors.map(r => (
                <div key={r._row} className="text-xs text-red-600">
                  <strong>Row {r._row}:</strong> {r._errors.join('; ')}
                </div>
              ))}
            </div>
          )}

          {/* Preview table */}
          <div>
            <p className="text-xs font-semibold text-gray-500 mb-2">Preview (first 5 valid rows)</p>
            <div className="overflow-x-auto border border-gray-200 rounded-xl">
              <table className="w-full text-xs">
                <thead className="bg-gray-50 border-b border-gray-200">
                  <tr>
                    {['Name', 'Gender', 'Class', 'Combination', 'Year'].map(h => (
                      <th key={h} className="text-left px-3 py-2 font-semibold text-gray-600">{h}</th>
                    ))}
                  </tr>
                </thead>
                <tbody className="divide-y divide-gray-100">
                  {rows.filter(r => !r._errors.length).slice(0,5).map((r, i) => (
                    <tr key={i} className="hover:bg-gray-50">
                      <td className="px-3 py-2 text-gray-800">{r.full_name}</td>
                      <td className="px-3 py-2 text-gray-600">{r.gender || '—'}</td>
                      <td className="px-3 py-2 text-gray-600">{r.class_name}</td>
                      <td className="px-3 py-2 text-gray-600">{r.combination_code || '—'}</td>
                      <td className="px-3 py-2 text-gray-600">{r.enrollment_year || '—'}</td>
                    </tr>
                  ))}
                </tbody>
              </table>
            </div>
          </div>

          <div className="flex gap-3">
            <button onClick={() => { setStep('upload'); setRows([]); setErrors([]); if(fileRef.current) fileRef.current.value='' }}
              className="flex-1 border border-gray-300 text-gray-700 rounded-xl py-2.5 text-sm font-medium hover:bg-gray-50 transition">
              ← Back
            </button>
            <button onClick={handleImport} disabled={importing || rows.filter(r=>!r._errors.length).length === 0}
              className="flex-1 bg-green-700 hover:bg-green-600 text-white rounded-xl py-2.5 text-sm font-semibold transition disabled:opacity-60 flex items-center justify-center gap-2">
              {importing ? (
                <><svg className="animate-spin h-4 w-4" viewBox="0 0 24 24" fill="none">
                  <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"/>
                  <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8v8z"/>
                </svg> Importing…</>
              ) : `Import ${rows.filter(r=>!r._errors.length).length} student(s)`}
            </button>
          </div>
        </div>
      )}
    </>
  )
}

