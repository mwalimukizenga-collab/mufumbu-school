// ── CSV helpers ───────────────────────────────────────────────────────────────
const CSV_HEADERS = ['full_name','gender','date_of_birth','class_name','combination_code','enrollment_year']

function downloadTemplate() {
  const rows = [
    CSV_HEADERS.join(','),
    `John Doe,M,2008-01-15,Form 1,,${CURRENT_YEAR}`,
    `Jane Smith,F,2008-03-22,Form 2,,${CURRENT_YEAR}`,
    `Alice Mbeki,F,2006-07-10,Form 5,PCB,${CURRENT_YEAR}`,
  ]
  const blob = new Blob([rows.join('\n')], { type: 'text/csv' })
  const a = Object.assign(document.createElement('a'), {
    href: URL.createObjectURL(blob),
    download: 'mufumbu_students_template.csv',
  })
  a.click()
  URL.revokeObjectURL(a.href)
}

function parseCSV(text) {
  const lines = text.trim().split(/\r?\n/)
  if (lines.length < 2) return { headers: [], rows: [] }
  const headers = lines[0].split(',').map(h => h.trim().toLowerCase().replace(/\s+/g,'_'))
  const rows = lines.slice(1).filter(l => l.trim()).map((line, i) => {
    // handle quoted fields with commas
    const values = []
    let cur = '', inQ = false
    for (const ch of line) {
      if (ch === '"') { inQ = !inQ }
      else if (ch === ',' && !inQ) { values.push(cur.trim()); cur = '' }
      else cur += ch
    }
    values.push(cur.trim())
    return { _row: i + 2, ...Object.fromEntries(headers.map((h, j) => [h, values[j] ?? ''])) }
  })
  return { headers, rows }
}

